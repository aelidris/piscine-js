  export function generateClasses(){
    const styleTag = document.createElement('style');
    let css = '';
    colors.forEach(color => {
      css += `.${color} {background: ${color};}\n`
    });
    styleTag.textContent = css;
    document.head.appendChild(styleTag);
  }
  
  export function generateColdShades(){
    const coldShades = ['aqua', 'blue', 'turquoise', 'green', 'cyan', 'navy', 'purple'];
    const body = document.querySelector('body');
    colors.forEach(color => {
      if (coldShades.some(shade => color.includes(shade))){
        const colorDiv = document.createElement('div');
        colorDiv.className = color;
        colorDiv.textContent = color;
        body.appendChild(colorDiv);
      }
    });
  }
  
  export function choseShade(selectedShade){
    const allDivs = document.querySelectorAll('div');
    allDivs.forEach(div => {
      div.className = '';
      div.classList.add(selectedShade);
    });
  }