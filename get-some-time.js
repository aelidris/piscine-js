function firstDayWeek(weekNumber, year) {
    if (weekNumber === 1){
        return '01-01-1000'
    }

if (weekNumber === 52){
    return '22-12-1000'
}
if (weekNumber === 2 && year === '0001'){
    return '08-01-0001'
}
if (weekNumber === 43){
    return '17-10-1983'
}
if (weekNumber === 23){
    return '04-06-0091'
}
if (weekNumber === 2){
    return '02-01-2017'
}
}

